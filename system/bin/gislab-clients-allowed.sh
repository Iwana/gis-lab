#!/bin/bash
# Author Ivan Mincik, GISTA s.r.o., ivan.mincik@gmail.com

set -e

source /vagrant/config.cfg
if [ -f /vagrant/config-user.cfg ]
then
	source /vagrant/config-user.cfg
fi

echo -e "\n[gis.lab]: Creating allowed MACs list ...\n"

echo "# This file was automatically generated by gis.lab script ($(date))." > /etc/ltsp/dhcpd-clients-allowed.conf
echo "group {" >> /etc/ltsp/dhcpd-clients-allowed.conf

for mac in "${GISLAB_CLIENTS_ALLOWED[@]}"
do
	echo $mac
	hostname=$(echo $mac | sed 's/://g')
	echo -e "\thost $hostname { hardware ethernet $mac; }"  >> /etc/ltsp/dhcpd-clients-allowed.conf
done

echo "}" >> /etc/ltsp/dhcpd-clients-allowed.conf

service isc-dhcp-server restart

echo -e "\n[gis.lab]: Done."


